let persons = ["Amir","Abolfazl","Mahdy","Parham"];
// for loop by step +
for(let i = 0;i < persons.length;i++){
    console.log(`Person's  name : ${persons[i]}`);
}

console.log("--------------------")

// for loop by step -
for(let i = persons.length -1;i > -1;i--){
    console.log(`Person's  name : ${persons[i]}`);
}